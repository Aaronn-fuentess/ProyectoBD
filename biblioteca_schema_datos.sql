CREATE TABLE autores (
    id_autor INT GENERATED ALWAYS AS IDENTITY,
    nombre_autor VARCHAR(100) NOT NULL,
    nacionalidad VARCHAR(100),
    PRIMARY KEY (id_autor)
);

CREATE TABLE editoriales (
    id_editorial INT GENERATED ALWAYS AS IDENTITY,
    nombre_editorial VARCHAR(150) NOT NULL,
    pais VARCHAR(50),
    PRIMARY KEY (id_editorial)
);

CREATE TABLE categorias (
    id_categoria INT GENERATED ALWAYS AS IDENTITY,
    nombre_categoria VARCHAR(100) NOT NULL,
    PRIMARY KEY (id_categoria)
);

CREATE TABLE empleados (
    id_empleado INT GENERATED ALWAYS AS IDENTITY,
    nombre VARCHAR(100) NOT NULL,
    cargo VARCHAR(50),
    PRIMARY KEY (id_empleado)
);

CREATE TABLE socios (
    id_socio INT GENERATED ALWAYS AS IDENTITY,
    nombre VARCHAR(100) NOT NULL,
    correo VARCHAR(100) UNIQUE,
    esta_solvente BOOLEAN DEFAULT TRUE,
    PRIMARY KEY (id_socio)
);

CREATE TABLE libros (
    isbn VARCHAR(20),
    titulo VARCHAR(255) NOT NULL,
    id_editorial INT,
    id_categoria INT,
    anio_publicacion INT,
    PRIMARY KEY (isbn),
    FOREIGN KEY (id_editorial) REFERENCES editoriales(id_editorial) 
        ON DELETE SET NULL ON UPDATE CASCADE,
    FOREIGN KEY (id_categoria) REFERENCES categorias(id_categoria) 
        ON DELETE SET NULL ON UPDATE CASCADE
);

CREATE TABLE libro_autor (
    isbn VARCHAR(20),
    id_autor INT,
    PRIMARY KEY (isbn, id_autor),
    FOREIGN KEY (isbn) REFERENCES libros(isbn) 
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_autor) REFERENCES autores(id_autor) 
        ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE ejemplares (
    id_ejemplar INT GENERATED ALWAYS AS IDENTITY,
    isbn VARCHAR(20),
    estado VARCHAR(20) DEFAULT 'Disponible',
    PRIMARY KEY (id_ejemplar),
    FOREIGN KEY (isbn) REFERENCES libros(isbn) 
        ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT chk_estado_ejemplar 
        CHECK (estado IN ('Disponible', 'Prestado', 'Reservado', 'Mantenimiento'))
);

CREATE TABLE prestamos (
    id_prestamo INT GENERATED ALWAYS AS IDENTITY,
    id_ejemplar INT,
    id_socio INT,
    id_empleado INT,
    fecha_salida DATE DEFAULT CURRENT_DATE,
    fecha_limite DATE NOT NULL,
    PRIMARY KEY (id_prestamo),
    FOREIGN KEY (id_ejemplar) REFERENCES ejemplares(id_ejemplar)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (id_socio) REFERENCES socios(id_socio)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (id_empleado) REFERENCES empleados(id_empleado)
        ON DELETE RESTRICT ON UPDATE CASCADE
);

CREATE TABLE devoluciones (
    id_devolucion INT GENERATED ALWAYS AS IDENTITY,
    id_prestamo INT,
    fecha_entrega_real DATE DEFAULT CURRENT_DATE,
    observaciones TEXT,
    PRIMARY KEY (id_devolucion),
    FOREIGN KEY (id_prestamo) REFERENCES prestamos(id_prestamo)
        ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE multas (
    id_multa INT GENERATED ALWAYS AS IDENTITY,
    id_socio INT,
    id_prestamo INT,
    monto DECIMAL(10,2) NOT NULL,
    estado_pago BOOLEAN DEFAULT FALSE,
    PRIMARY KEY (id_multa),
    FOREIGN KEY (id_socio) REFERENCES socios(id_socio)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_prestamo) REFERENCES prestamos(id_prestamo)
        ON DELETE CASCADE ON UPDATE CASCADE
);

